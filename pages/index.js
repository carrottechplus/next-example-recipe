import Head from 'next/head';
import styles from './Home.module.scss';
import clsx from 'clsx';
import axios from 'axios';
import Image from 'next/image';
axios.defaults.baseURL = 'https://www.themealdb.com/api/json/v1/1';

//www.themealdb.com/api/json/v1/1/filter.php?c=Seafood

export default function Home({ meals }) {
	const newMeals = meals.slice(0, 6);

	return (
		<>
			<Head>
				<title>NEXT Recipe</title>
				<meta name='description' content='Generated by create next app' />
				<meta name='viewport' content='width=device-width, initial-scale=1' />
				<link rel='icon' href='/favicon.ico' />
			</Head>
			<main className={clsx(styles.main)}>
				<h1>MAIN PAGE</h1>

				<figure className='visual'>
					<article className='bg'>
						{newMeals.map((item) => (
							<div key={item.idMeal} className='pic' style={{ position: 'relative', width: 400, height: 300 }}>
								<Image
									src={item.strMealThumb}
									alt={item.strMeal}
									priority
									fill
									quality={50}
									style={{ objectFit: 'cover' }}
								/>
							</div>
						))}
					</article>
					<article className='list'>
						{newMeals.map((item) => (
							<h2 key={item.idMeal}>{item.strMeal}</h2>
						))}
					</article>
				</figure>
			</main>
		</>
	);
}

export async function getStaticProps() {
	// props로 데이터를 넘길때에는, data안쪽의 값까지 뽑아낸 다음에 전달!!
	const { data } = await axios.get('/filter.php?c=Seafood');
	console.log('data fetching on Server', data);
	return {
		props: data,
		revalidate: 60 * 60 * 24, //이 부분 없으면 ssg
	};
}
